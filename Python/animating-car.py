#This script draw a car with beautiful animation using python turtle graphics 

from turtle import *
import time

width(6)
speed('slow')
bgcolor("#96ffb2")
def rectangle():
    penup()
    fillcolor('blue')
    begin_fill()
    goto(-200,0)
    pendown()
    forward(500) 
    left(90) 
 
    forward(100)
    left(90) 
    

    forward(500) 
    left(90) 
    
    forward(100) 
    left(90)
    end_fill()
time.sleep(2)
rectangle()
penup()
setx(-200)
sety(0)
pendown()
forward(50)
penup()
goto(-100,-50)

def wheel(x):
    for i in range(10):
        penup()
        goto(x,0)
        pendown()
        left(36)
        forward(50)
        backward(50)
def cir():
    pendown()
    color("black","orange")
    begin_fill()
    circle(50)
    end_fill()
    hideturtle()
cir()
wheel(-100)
goto(-50,0)
pendown()
forward(200)
penup()
goto(200,-50)
cir()
wheel(200)
goto(250,0)
pendown()
forward(50)
left(90)
forward(100)
left(90)
forward(100)
right(60)
forward(100)
left(60)
forward(200)
left(60)
forward(100)
left(120)
fillcolor('orange')
forward(150)
left(90)
forward(88)
backward(88)
right(90)
forward(150)
backward(400)
right(90)
forward(100)
penup()
goto(-300,-50)
pendown()
left(90)
forward(700)
pendown()
end_fill()

penup()
goto(300,65)
fillcolor("yellow")
begin_fill()
pendown()
circle(15,-180)
end_fill()
done()


